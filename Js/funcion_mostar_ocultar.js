addEventListener("DOMContentLoaded", (e) => {
    selec_opc = document.querySelector("#selec_opc");
    selec_opc.addEventListener("change", (e) => {
        var opc = innerText = selec_opc.value;
        if (opc == "Calcular el area del Triángulo") {
            document.querySelector("#form_04_1").style.display = 'block';
            document.querySelector("#form_04_2").style.display = 'none';
        } else if (opc == "Calcular el area del Círculo") {
            document.querySelector("#form_04_1").style.display = 'none';
            document.querySelector("#form_04_2").style.display = 'block';
        }else{
            document.querySelector("#form_04_1").style.display = 'none';
            document.querySelector("#form_04_2").style.display = 'none';
        }
    })
});